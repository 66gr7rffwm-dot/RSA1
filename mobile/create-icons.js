const fs = require('fs');
const path = require('path');

// Create a simple 1x1 PNG as placeholder (will be replaced later)
const createPlaceholderPNG = (size, filename) => {
  // Create a minimal valid PNG (1x1 pixel, green)
  const png = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1 dimensions
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xDE, // bit depth, color type, etc.
    0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, 0x54, // IDAT chunk
    0x08, 0x99, 0x01, 0x01, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x02, // image data (green pixel)
    0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82 // IEND
  ]);
  
  fs.writeFileSync(path.join(__dirname, 'assets', filename), png);
  console.log(`Created ${filename}`);
};

createPlaceholderPNG(1024, 'icon.png');
createPlaceholderPNG(2048, 'splash.png');
console.log('Placeholder icons created');
